# Definiera funktionen "hälsa" som tar ett nam som argument
def hälsa(namn):
    # Skapa välkomstmeddelande och returnera det
    meddelande = "Hej, " + namn + "! Välkommen!"
    return meddelande

# Anropa funktionen "hälsa" och spara resultate i en variabel
resultat = hälsa("Kenny")

#Skriv ut resultatet
print(resultat)

